#include "ft_printf.h"

int	check_flag(char flag_c)
{
	if (flag_c == 'c' || flag_c == 's' || flag_c == 'p'
		|| flag_c == 'd' || flag_c == 'i' || flag_c == 'u'
		|| flag_c == 'x' || flag_c == 'X' || flag_c == '%')
		return (1);
	return (0);
}
